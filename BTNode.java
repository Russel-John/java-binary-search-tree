public class BTNode
{
   BTNode rLink;
   int data;
   BTNode lLink;
   
   public BTNode()
   {
      this.rLink = rLink;
      this.data = data;
      this.lLink = lLink;
   }
   public BTNode(BTNode rlink, int info, BTNode llink) 
   {
      this.rLink = rlink;
      this.data = info;
      this.lLink = llink;
   }  
}

